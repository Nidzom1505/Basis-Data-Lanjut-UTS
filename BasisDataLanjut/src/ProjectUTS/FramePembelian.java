    private void tfJumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfJumlahActionPerformed
        String selectedItem = (String) cbList.getSelectedItem();
        if (selectedItem != null) {
            String[] parts = selectedItem.split("  \\|  ");
            if (parts.length == 3) {
                String hargaString = parts[1];
                try {
                    double harga = Double.parseDouble(hargaString);
                    int jumlah = Integer.parseInt(tfJumlah.getText());
                    double total = harga * jumlah;
                    tfTotal.setText(String.valueOf(total));
                } catch (NumberFormatException e) {
                    tfTotal.setText("Invalid Input");
                }
            }
        }
    }//GEN-LAST:event_tfJumlahActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            conn = DBConnection.getConnection();
            conn.setAutoCommit(false);

            if (tfJumlah.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Isi semua!");
            } else {
                String jumlah = tfJumlah.getText().trim();
                int jumlahTiket = Integer.parseInt(jumlah);

                String cat = cbList.getSelectedItem().toString();

                String checkKapasitasSql = "SELECT kapasitas FROM DaftarCAT WHERE idCAT = ?";
                PreparedStatement checkKapasitasPstmt = conn.prepareStatement(checkKapasitasSql);
                checkKapasitasPstmt.setInt(1, idcat);
                ResultSet rs = checkKapasitasPstmt.executeQuery();

                if (rs.next()) {
                    int kapasitas = rs.getInt("kapasitas");
                    if (jumlahTiket > kapasitas) {
                        JOptionPane.showMessageDialog(null, "Jumlah tiket yang ingin dibeli melebihi kapasitas yang tersedia!");
                        conn.rollback();
                        return;
                    }
                }

                String sql = "INSERT INTO Transaksi (idCAT, nik, jumlah_tiket, total_harga, tanggalPembelian, harga) VALUES (?,?,?,?,?,?)";
                pstmt = conn.prepareStatement(sql);

                String nik = tfnik.getText().trim();
                String hartot = tfTotal.getText();
                Timestamp timestamp = new Timestamp(System.currentTimeMillis());
                String nama = tfnama.getText();

                pstmt.setInt(1, idcat);
                pstmt.setString(2, nik);
                pstmt.setLong(3, jumlahTiket);
                pstmt.setBigDecimal(4, new BigDecimal(hartot));
                pstmt.setTimestamp(5, timestamp);
                pstmt.setBigDecimal(6, new BigDecimal(harga));

                // Konfirmasi sebelum menambahkan tiket
                String pesan = "Apakah Anda yakin ingin menambahkan tiket?\n"
                        + "Nama Pembeli: " + nama + "\n"
                        + "NIK Pembeli: " + nik + "\n"
                        + "Kategori: " + cat + "\n"
                        + "Jumlah: " + jumlah + "\n"
                        + "Total Harga: " + hartot;

                int jawab = JOptionPane.showConfirmDialog(null, pesan, "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE);
                switch (jawab) {
                    case JOptionPane.YES_OPTION:
                        pstmt.executeUpdate();

                        String updateKapasitasSql = "UPDATE DaftarCAT SET kapasitas = kapasitas - ? WHERE idCAT = ?";
                        PreparedStatement updateKapasitasPstmt = conn.prepareStatement(updateKapasitasSql);
                        updateKapasitasPstmt.setInt(1, jumlahTiket);
                        updateKapasitasPstmt.setInt(2, idcat);
                        updateKapasitasPstmt.executeUpdate();

                        conn.commit();
                        JOptionPane.showMessageDialog(null, "Pembelian berhasil!");

                        tfJumlah.setText("");
                        cbList.setSelectedIndex(0);
                        tfTotal.setText("");
                        loadComboBoxData();
                        break;
                    case JOptionPane.NO_OPTION:
                        break;
                    default:
                        break;
                }
            }
        } catch (SQLException e) {
            try {
                if (conn != null) {
                    conn.rollback();
                }
                throw e;
            } catch (SQLException ex) {
                Logger.getLogger(BuatAkun.class.getName()).log(Level.SEVERE, null, ex);
            }
        } finally {
            try {
                if (conn != null && !conn.isClosed()) {
                    conn.setAutoCommit(true);
                    conn.close();
                }
            } catch (SQLException e) {
                Logger.getLogger(FramePembelian.class.getName()).log(Level.SEVERE, null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

