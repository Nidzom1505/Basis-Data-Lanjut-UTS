    public void showTable() {
        tbmmhs = new DefaultTableModel(new String[]{"ID PEMBELIAN", "NAMA", "EMAIL", "CAT", "HARGA", "JUMLAH", "HARGA TOTAL", "TANGGAL"}, 0);
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;

        try {
            conn = DBConnection.getConnection();
            String sql = "SELECT \n"
                    + "    T.idTransaksi,\n"
                    + "    P.nama AS namaPembeli,\n"
                    + "    P.email AS emailPembeli,\n"
                    + "    D.namaCAT,\n"
                    + "    T.harga,\n"
                    + "    T.jumlah_tiket,\n"
                    + "    T.total_harga,\n"
                    + "    T.tanggalPembelian\n"
                    + "FROM \n"
                    + "    Transaksi T\n"
                    + "JOIN \n"
                    + "    Pembeli P ON T.nik = P.nik\n"
                    + "JOIN \n"
                    + "    DaftarCAT D ON T.idCAT = D.idCAT;";

            stmt = conn.createStatement();
            rs = stmt.executeQuery(sql);

            while (rs.next()) {
                tbmmhs.addRow(new Object[]{
                    rs.getInt("idTransaksi"),
                    rs.getString("namaPembeli").trim(),
                    rs.getString("emailPembeli").trim(),
                    rs.getString("namaCAT").trim(),
                    rs.getBigDecimal("harga"),
                    rs.getInt("jumlah_tiket"),
                    rs.getBigDecimal("total_harga"),
                    rs.getString("tanggalPembelian").trim()});
            }

        } catch (SQLException e) {
            try {
                if (conn != null) {
                    conn.rollback();
                }
                throw e;
            } catch (SQLException ex) {
                Logger.getLogger(BuatAkun.class.getName()).log(Level.SEVERE, null, ex);
            }

        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (stmt != null) {
                    stmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrameAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        tabel.setModel(tbmmhs);
    }

