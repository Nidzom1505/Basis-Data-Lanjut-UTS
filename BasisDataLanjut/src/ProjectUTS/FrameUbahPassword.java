    private void cariData(String input) {
        try {
            Connection conn = DBConnection.getConnection();
            conn.setAutoCommit(false);

            String sql = "SELECT * FROM pembeli WHERE nik = ? OR email = ?";
            pstmt = conn.prepareStatement(sql);

            input = tfNIK.getText().trim();

            pstmt.setString(1, input);
            pstmt.setString(2, input);

            ResultSet rs = pstmt.executeQuery();
            boolean found = false;

            while (rs.next()) {
                if (rs.getString("nik").equals(input) || rs.getString("email").equals(input)) {
                    JOptionPane.showMessageDialog(null, "Data Ditemukan. Silahkan Ganti Password.");
                    found = true;
                    break;
                }
            }

            if (!found) {
                JOptionPane.showMessageDialog(null, "Data Tidak Terdaftar.");
            }

            conn.commit();
            conn.setAutoCommit(true);
            conn.close();

        } catch (SQLException e) {
            try {
                if (conn != null) {
                    conn.rollback();
                }
                throw e;
            } catch (SQLException ex) {
                Logger.getLogger(BuatAkun.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

