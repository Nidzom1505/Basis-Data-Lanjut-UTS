    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        DefaultTableModel ob = (DefaultTableModel) tabel.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(ob);
        tabel.setRowSorter(sorter);

        int selectedIndex = cbFilter.getSelectedIndex();
        switch (selectedIndex) {
            case 0:
                sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(7, SortOrder.DESCENDING)));
                break;
            case 1:
                sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(7, SortOrder.ASCENDING)));
                break;
            case 2:
                sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(1, SortOrder.ASCENDING)));
                break;
            case 3:
                sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(1, SortOrder.DESCENDING)));
                break;
            case 4:
                sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(4, SortOrder.ASCENDING)));
                break;
            case 5:
                sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(4, SortOrder.DESCENDING)));
                break;
            default:
                sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(0, SortOrder.ASCENDING)));
                break;
        }
        sorter.sort();
    }//GEN-LAST:event_jComboBox1ActionPerformed

