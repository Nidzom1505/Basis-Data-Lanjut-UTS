    private void loadComboBoxData() {
        try {
            conn = DBConnection.getConnection();
            conn.setAutoCommit(false);
            cbList.removeAllItems();

            String sql = "SELECT idCAT, namaCAT, harga, kapasitas FROM DaftarCAT ORDER BY idCAT;";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("idCAT");
                String nama = rs.getString("namaCAT");
                String harga = rs.getString("harga");
                int kapasitas = rs.getInt("kapasitas");

                if (kapasitas > 0) {
                    cbList.addItem(nama + "  |  " + harga + "  |  " + kapasitas);
                }
            }

            rs.close();
            pstmt.close();
            conn.close();

        } catch (SQLException e) {
            try {
                if (conn != null) {
                    conn.rollback();
                }
                throw e;
            } catch (SQLException ex) {
                Logger.getLogger(BuatAkun.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void date() {
        Timer timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date tanggalSekarang = new Date();
                SimpleDateFormat tanggalWaktu = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                String tanggal = tanggalWaktu.format(tanggalSekarang);
                lbTanggal.setText(tanggal);
            }
        });

        timer.start();
    }

