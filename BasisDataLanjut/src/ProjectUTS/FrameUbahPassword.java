    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection conn = DBConnection.getConnection();
            conn.setAutoCommit(false);
            if (tfNIK.getText().equals("") | tfPW.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Isi semua data");
            } else {
                String sql = "UPDATE pembeli SET password = ? WHERE nik = ? or email = ?";
                pstmt = conn.prepareStatement(sql);

                String input, pw;
                input = tfNIK.getText().trim();
                pw = tfPW.getText().trim();

                pstmt.setString(1, pw);
                pstmt.setString(2, input);
                pstmt.setString(3, input);

                pstmt.executeUpdate();
                conn.commit();
                conn.setAutoCommit(true);
                conn.close();

                tfNIK.setText("");
                tfPW.setText("");
            }
        } catch (SQLException e) {
            try {
                if (conn != null) {
                    conn.rollback();
                }
                throw e;
            } catch (SQLException ex) {
                Logger.getLogger(BuatAkun.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

